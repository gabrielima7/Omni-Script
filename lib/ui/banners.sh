# Box Drawing
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
draw_box() {
    local title="$1"
    local width="${2:-60}"
    local color="${3:-$ELECTRIC_PURPLE}"
    
    local inner_width=$((width - 2))
    local title_len=${#title}
    local padding_left=$(( (inner_width - title_len) / 2 ))
    local padding_right=$(( inner_width - title_len - padding_left ))
    
    echo -e "${color}${BOX_TL}$(printf "${BOX_H}%.0s" $(seq 1 $inner_width))${BOX_TR}${RST}"
    echo -e "${color}${BOX_V}${RST}$(printf '%*s' "$padding_left" '')${BOLD}${title}${RST}$(printf '%*s' "$padding_right" '')${color}${BOX_V}${RST}"
    echo -e "${color}${BOX_BL}$(printf "${BOX_H}%.0s" $(seq 1 $inner_width))${BOX_BR}${RST}"
}
